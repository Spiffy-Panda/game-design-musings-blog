def shoot(browser, url, out_png, vw, vh):
    import os
    import tempfile
    profile = tempfile.mkdtemp(prefix="edge_vid_")
    args = [browser, "--headless=new", "--disable-gpu", "--no-sandbox",
            "--hide-scrollbars", "--force-device-scale-factor=1",
            "--run-all-compositor-stages-before-draw",
            f"--window-size={vw},{vh}", f"--user-data-dir={profile}",
            f"--screenshot={out_png}", url]
    subprocess.run(args, capture_output=True)
    shutil.rmtree(profile, ignore_errors=True)
    if not Path(out_png).exists():
        sys.exit(f"screenshot failed: {url}")


def file_url(p: Path, anchor=""):
    u = "file:///" + str(p).replace("\\", "/")
    return u + (("#" + anchor) if anchor else "")


def find_piper():
    for c in PIPER_EXES:
        if not c:
            continue
        if Path(c).exists():
            return c
        w = shutil.which(c)
        if w:
            return w
    sys.exit("Piper not found. Set PIPER_EXE or install piper.exe under %LOCALAPPDATA%\\piper.")


def synth(text, out_wav, backend, voice, rate):
    if backend == "piper":
        exe = find_piper()
        model = voice if voice.endswith(".onnx") else str(PIPER_VOICES / f"{voice}.onnx")
        if not Path(model).exists():
            sys.exit(f"Piper voice model not found: {model}")
        subprocess.run([exe, "-m", model, "-f", str(out_wav)],
                       input=text, text=True, capture_output=True, check=True)
    else:  # sapi
        subprocess.run(
            ["powershell", "-NoProfile", "-ExecutionPolicy", "Bypass", "-File", str(TTS_PS1),
             "-Text", text, "-Out", str(out_wav), "-Voice", voice, "-Rate", str(rate)],
            capture_output=True, check=True)
    if not out_wav.exists():
        sys.exit(f"tts ({backend}) failed for: {text[:40]}...")


def atempo_chain(speed):
    """ffmpeg atempo accepts 0.5..2.0 per stage; chain for larger factors."""
    if abs(speed - 1.0) < 1e-3:
        return None
    stages, s = [], speed
    while s > 2.0:
        stages.append("atempo=2.0")
        s /= 2.0
    while s < 0.5:
        stages.append("atempo=0.5")
        s /= 0.5
    stages.append(f"atempo={s:.4f}")
    return ",".join(stages)


def wav_seconds(ffprobe, wav):
    r = subprocess.run([ffprobe, "-v", "error", "-show_entries", "format=duration",
                        "-of", "default=nw=1:nk=1", str(wav)],
                       capture_output=True, text=True, check=True)
    return float(r.stdout.strip())


def make_clip(ffmpeg, png, wav, out_mp4, dur, idx, speed=1.0):
    frames = int(round(dur * FPS))
    # alternate gentle zoom direction so consecutive scenes don't feel identical
    if idx % 2 == 0:
        z = "min(zoom+0.00040,1.12)"
    else:
        z = "if(eq(on,0),1.12,max(zoom-0.00040,1.0))"
    vf = (
        f"scale=3840:2160:force_original_aspect_ratio=increase,crop=3840:2160,"
        f"zoompan=z='{z}':d={frames}:x='iw/2-(iw/zoom/2)':y='ih/2-(ih/zoom/2)':"
        f"s={W}x{H}:fps={FPS},"
        f"fade=t=in:st=0:d=0.4,fade=t=out:st={max(0,dur-0.4):.3f}:d=0.4,format=yuv420p"
    )
    tempo = atempo_chain(speed)
    af = (f"{tempo}," if tempo else "") + \
        f"apad,afade=t=in:st=0:d=0.25,afade=t=out:st={max(0,dur-0.4):.3f}:d=0.4"
    subprocess.run(
        [ffmpeg, "-y", "-loop", "1", "-framerate", str(FPS), "-t", f"{dur:.3f}", "-i", str(png),
         "-i", str(wav),
         "-filter_complex", f"[0:v]{vf}[v];[1:a]{af}[a]",
         "-map", "[v]", "-map", "[a]", "-t", f"{dur:.3f}",
         "-c:v", "libx264", "-preset", "medium", "-crf", "20", "-pix_fmt", "yuv420p",
         "-r", str(FPS), "-c:a", "aac", "-ar", "48000", "-b:a", "128k",
         str(out_mp4)],
        capture_output=True, check=True)


def add_music(ffmpeg, ffprobe, in_mp4, out_mp4, cfg):
    """Mix a procedural ambient binaural-beats bed under the narration of in_mp4."""
    dur = wav_seconds(ffprobe, in_mp4)  # ffprobe format=duration works on mp4 too
    fL = float(cfg["carrier"])
    fR = fL + float(cfg["beat"])
    drone = fL * 1.5  # a soft perfect-fifth drone, equal in both ears (keeps the binaural pair intact)
    g = float(cfg["gain"])
    fc = (
        f"[1:a][2:a]join=inputs=2:channel_layout=stereo[bin];"
        f"[3:a]aformat=channel_layouts=stereo[dr];"
        f"[bin]volume=0.5[binv];[dr]volume=0.18[drv];"
        f"[binv][drv]amix=inputs=2:normalize=0[bedmix];"
        f"[bedmix]tremolo=f=0.1:d=0.25,lowpass=f=700,aecho=0.8:0.7:60:0.3,"
        f"volume={g:.3f},afade=t=in:d=2,afade=t=out:st={max(0,dur-3):.2f}:d=3[bed];"
        f"[0:a]aformat=channel_layouts=stereo[narr];"
        f"[narr][bed]amix=inputs=2:normalize=0[a]"
    )
    subprocess.run(
        [ffmpeg, "-y", "-i", str(in_mp4),
         "-f", "lavfi", "-t", f"{dur:.3f}", "-i", f"sine=frequency={fL}:sample_rate=48000",
         "-f", "lavfi", "-t", f"{dur:.3f}", "-i", f"sine=frequency={fR}:sample_rate=48000",
         "-f", "lavfi", "-t", f"{dur:.3f}", "-i", f"sine=frequency={drone}:sample_rate=48000",
         "-filter_complex", fc, "-map", "0:v", "-map", "[a]",
         "-c:v", "copy", "-c:a", "aac", "-ar", "48000", "-b:a", "160k", str(out_mp4)],
        capture_output=True, check=True)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("slug")
    ap.add_argument("--browser", default="edge", choices=list(BROWSERS))
    ap.add_argument("--tts", choices=["sapi", "piper"], help="override the scene script's TTS backend")
    ap.add_argument("--speed", type=float, help="override narration tempo (atempo); >1 = faster")
    ap.add_argument("--no-music", action="store_true", help="disable the ambient binaural bed")
    ap.add_argument("--keep", action="store_true", help="keep the per-scene build dir")
    args = ap.parse_args()

    spec_path = SCENES_DIR / f"{args.slug}.json"
    if not spec_path.exists():
        sys.exit(f"no scene script: {spec_path}")
    spec = json.loads(spec_path.read_text(encoding="utf-8"))
    page = REPO / "explorations" / args.slug / "index.html"
    if not page.exists():
        sys.exit(f"no exploration page: {page}")

    browser = find_browser(args.browser)
    ffmpeg, ffprobe = tool("ffmpeg"), tool("ffprobe")
    backend = args.tts or spec.get("tts", "sapi")
    default_voice = "en_US-amy-medium" if backend == "piper" else "Microsoft Zira Desktop"
    voice = spec.get("voice", default_voice) if backend == spec.get("tts", "sapi") else default_voice
    rate = int(spec.get("rate", 0))
    speed = args.speed if args.speed else float(spec.get("speed", DEFAULT_SPEED))
    music = None
    if not args.no_music and spec.get("music") is not False:
        music = {**DEFAULT_MUSIC, **(spec.get("music") if isinstance(spec.get("music"), dict) else {})}

    bdir = BUILD_ROOT / args.slug
    if bdir.exists():
        shutil.rmtree(bdir, ignore_errors=True)
    bdir.mkdir(parents=True, exist_ok=True)
    OUT_DIR.mkdir(parents=True, exist_ok=True)

    clips = []
    total = 0.0
    for i, sc in enumerate(spec["scenes"]):
        sid = f"{i:02d}_{sc['id']}"
        png = bdir / f"{sid}.png"
        wav = bdir / f"{sid}.wav"
        clip = bdir / f"{sid}.mp4"

        if sc["type"] == "page":
            vw, vh = sc.get("viewport", [1280, 800])
            shoot(browser, file_url(page, sc.get("anchor", "")), str(png), vw, vh)
        else:
            html = bdir / f"{sid}.html"
            html.write_text(slide_html(sc, spec), encoding="utf-8")
            shoot(browser, file_url(html), str(png), W, H)

        synth(sc["narration"], wav, backend, voice, rate)
        dur = max(MIN_SCENE, wav_seconds(ffprobe, wav) / speed + TAIL)
        make_clip(ffmpeg, png, wav, clip, dur, i, speed)
        clips.append(clip)
        total += dur
        print(f"  scene {sid}: {dur:5.1f}s")

    listf = bdir / "concat.txt"
    listf.write_text("".join(f"file '{c.as_posix()}'\n" for c in clips), encoding="utf-8")
    out_mp4 = OUT_DIR / f"{args.slug}.mp4"
    if music:
        concat_mp4 = bdir / "_concat.mp4"
        subprocess.run([ffmpeg, "-y", "-f", "concat", "-safe", "0", "-i", str(listf),
                        "-c", "copy", str(concat_mp4)], capture_output=True, check=True)
        add_music(ffmpeg, ffprobe, concat_mp4, out_mp4, music)
    else:
        subprocess.run([ffmpeg, "-y", "-f", "concat", "-safe", "0", "-i", str(listf),
                        "-c", "copy", str(out_mp4)], capture_output=True, check=True)

    if not args.keep:
        shutil.rmtree(bdir, ignore_errors=True)
    bed = f"binaural {music['carrier']:.0f}/{music['carrier']+music['beat']:.0f}Hz" if music else "none"
    print(f"\nDONE  {out_mp4}   (~{total:.0f}s, {len(clips)} scenes, tts={backend}, speed={speed}, music={bed})")


if __name__ == "__main__":
    main()
