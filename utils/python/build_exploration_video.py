def find_browser(pref):
    order = [pref] + [b for b in BROWSERS if b != pref]
    for name in order:
        for p in BROWSERS[name]:
            if Path(p).exists():
                return p
    sys.exit("No Edge/Chrome found for headless rendering.")


def tool(name):
    """Resolve ffmpeg/ffprobe from PATH, else the repo's known install dir."""
    p = shutil.which(name)
    if p:
        return p
    cand = Path(r"C:\ffmpeg\bin") / (name + ".exe")
    if cand.exists():
        return str(cand)
    sys.exit(f"{name} not found on PATH or C:\\ffmpeg\\bin")


