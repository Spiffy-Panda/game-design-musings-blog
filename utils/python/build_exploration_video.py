#!/usr/bin/env python3
"""
build_exploration_video.py — turn one MSL "exploration" page into a narrated
60-90s explainer video (visuals + audio), with zero pip installs.

Pipeline (all building blocks ship with Windows + the repo's ffmpeg):
  1. Read a scene script:  explorations/_video/scenes/<slug>.json
  2. Per scene, render a 1920x1080 still:
       - type "slide": build an ops-console-styled HTML slide -> screenshot (Edge headless)
       - type "page" : screenshot the real exploration page (optionally at an #anchor)
  3. Synthesize narration WAV per scene via utils/powershell/tts_sapi.ps1 (Windows SAPI).
  4. Build a per-scene clip: ffmpeg loops the still with a slow Ken-Burns zoom,
     muxes the narration, fades in/out. Clip length = narration + tail.
  5. Concat clips -> explorations/_video/out/<slug>.mp4

Why this shape: the live JS instruments on each page do NOT paint in one-shot
headless capture (they draw in animation frames the screenshotter never runs),
so the diagrams are re-drawn as static SVG inside the slides, and only the
*static* page content (hero/prose) is screenshotted directly. See DEV-LOG.

Usage (run from anywhere; the script anchors to the repo root):
  python utils/python/build_exploration_video.py <slug> [--keep] [--browser edge|chrome]
  python utils/python/build_exploration_video.py liquidity-deflation-spiral

Rule 1: this file anchors to the repo root via Path(__file__).resolve().parents[2]
and never assumes the invocation directory.
"""

